<strong>Creating a Task</strong>
<p>
	To create a Task, simply click the 'Create Task' button. <br /> This
	will open another modal like this one.
</p>
<p>Creating a task consists of 3 fields</p>
<ul>
	<li><strong>Title - </strong>This is the title of your task.</li>
	<li><strong>Description - </strong>Provide a brief description of your
		task here.</li>
	<li><strong>Status - </strong>The current status of the task.</li>
</ul>
<p>Once you have filled out all of these details, click the 'Create
	Task' button inside the modal. This should automatically close the
	modal. You will see your new task slide into the task table once the
	task has successfully been saved to the database.</p>
<br />
<strong>Opening a Task</strong>
<p>To open a Task, simply click on the title of the task you wish to
	view. This will load the Task in a new page.</p>
<br />
<strong>Filtering the Tasks</strong>
<p>If you are looking for all Tasks with a certain status, you are able
	to filter the Tasks shown in the table by doing these 3 simple steps:
<ul>
	<li>Click the filter drop down</li>
	<li>Select which status to filter by</li>
	<li>Click the 'Filter' button</li>
</ul>
Once you have clicked the 'Filter' button, the table will update and
show all Task's with the status that you requested.
</p>
<br />
<strong>Navigating through the Table</strong>
<p>
	To keep your data costs down (your welcome mobile users) the Task
	system only loads 5 Tasks at a time.<br /> Each time that you click a
	new page using the paginator below the list of Tasks, 5 more are loaded
	(for the requested page) and then stored locally. This is so we don't
	have to keep loading the Tasks each time we change page.<br /> You may
	notice that each time you load a new page of Tasks it takes a second or
	2 to load, then the second time you go to that page it loads instantly.
	That means the Task system is working correctly :).
</p>
