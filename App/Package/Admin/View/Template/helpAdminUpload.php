<strong>Adding a File</strong>
<p>
	To add a file, fill in the appropriate fields and press upload. You will be then taken back to the main admin page.<br /> If you wish to leave the page without saving simply navigate way from the page.
</p>
<p>An acceptable upload includes 2 fields:</p>
<ul>
	<li><strong>Title - </strong>This is the name of the file up wish to upload.<br/> It can accept numbers and letters.
	</li>
	<li>
		<strong>File to Upload - </strong> You can only upload one file at a time. When you click the "Choose File" button a file explorer will open. Choose the selected file and press open.
	</li>
</ul>

<p>Once you have filled out all of these, click 'Save' and you will be able to view the updated content on the admin page.</p>
